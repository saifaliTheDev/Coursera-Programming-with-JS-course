for (var i = 1; i <= 5; i++) {
  console.log(i);
}
console.log("counting finished!");

for (var i = 5; i > 0; i--) {
  console.log(i);
}
console.log("countdown finished!");

var i = 1;
while (i <= 5) {
  console.log(i);
  i = i + 1;
}
console.log("counting finished");

var i = 5;
while (i > 0) {
  console.log(i);
  i = i - 1;
}
console.log("countdown finished");

